/**
 * Get a translation string for the current language
 */
export function t<T extends Record<SupportedLanguage, string>>(
  translations: T,
  lang?: SupportedLanguage
): string {
  const language = lang || getBrowserLanguage();
  return translations[language] || translations.en;
}
