/**
 * Centralized translations for the application.
 * Supported languages: es, en, de, fr, pt, pt-BR, ca
 */

export type SupportedLanguage = 'es' | 'en' | 'de' | 'fr' | 'pt' | 'pt-BR' | 'ca';

export const SUPPORTED_LANGUAGES: SupportedLanguage[] = ['es', 'en', 'de', 'fr', 'pt', 'pt-BR', 'ca'];

/**
 * Get the user's preferred language from the browser
 */
export function getBrowserLanguage(): SupportedLanguage {
  if (typeof navigator === 'undefined') return 'en';

  const browserLang = navigator.language;

  // Check for exact match first (e.g., pt-BR)
  if (browserLang === 'pt-BR') return 'pt-BR';

  // Get the base language code
  const baseLang = browserLang.split('-')[0].toLowerCase();

  // Map to supported languages
  if (baseLang === 'es') return 'es';
  if (baseLang === 'en') return 'en';
  if (baseLang === 'de') return 'de';
  if (baseLang === 'fr') return 'fr';
  if (baseLang === 'pt') return 'pt';
  if (baseLang === 'ca') return 'ca';

  // Default to English
  return 'en';
}

